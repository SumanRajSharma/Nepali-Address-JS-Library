function getZoneIDs(development_regionID) {
	return development_region_Zone_Array[development_regionID-1]
}

function getDistrictsIDs(zoneID){
	return zones_district_Array[zoneID-1]
}

function getMunicipalitiesIDs(districtID){
	return zones_municipalities_Array[districtID-1]
}

function NameArrayfromIDArray(IDs,infoArray)
{
	
	var names = [];
	for (var keys in infoArray)
	{
		
		if (IDs.indexOf(parseInt(infoArray[keys].id)) !== -1){
			names.push(infoArray[keys])
		}
	}
	return names



}


function update()
{
	
	switch(this.id)
	{
		case "developmentRegion":
		removeOptions("Zones")
		removeOptions("District")
		removeOptions("Municipalities")
		var selectd =  document.getElementById("Zones")
		var idOfAltered = document.getElementById("developmentRegion").value
		var IDs = getZoneIDs(idOfAltered)
		var zoneNames = NameArrayfromIDArray(IDs,zones)	
		break;

		case "Zones":
		removeOptions("District")
		removeOptions("Municipalities")
		var selectd =  document.getElementById("District")
		var idOfAltered = document.getElementById("Zones").value
		var IDs = getDistrictsIDs(idOfAltered)
		var zoneNames = NameArrayfromIDArray(IDs,districts)	
		break;

		case "District":
		removeOptions("Municipalities")
		var selectd =  document.getElementById("Municipalities")
		var idOfAltered = document.getElementById("District").value
		var IDs = getMunicipalitiesIDs(idOfAltered)
		var zoneNames = NameArrayfromIDArray(IDs,municipalities)	
		break;
		
		
	}
	for (var keys in zoneNames)
	{

		option = createOption(zoneNames[keys].id,zoneNames[keys].name)
		selectd.appendChild(option)
	}
}


function removeOptions(select)
{
	selectbox = document.getElementById(select)
	var i;
	for(i=selectbox.options.length-1;i>=0;i--)
	{
		selectbox.remove(i);

	}
	// lets add the header
	option = createOption(0,select)
	selectbox.appendChild(option)
}
function createOption(id, content)
{
	var option = document.createElement("option");
	option.value = id
	option.innerHTML = content

	return option
}

window.onload = function () {
	var select_development_regions =  document.getElementById("developmentRegion")
	for (var keys in development_regions)
		{			option = createOption(development_regions[keys].id,development_regions[keys].name)
			select_development_regions.appendChild(option)
		}
		
	}
	document.getElementById('developmentRegion').addEventListener('change',update,false);
	document.getElementById('Zones').addEventListener('change',update,false);
	document.getElementById('District').addEventListener('change',update,false);


